
=begin
Ruby also provides methods from the string class to manipulate strings.
 These methods can be called using the . (dot) operator after string.
  A few of these methods are length, reverse, upcase, and downcase.
=end

# Get the length of the string
str1 = "Health is Wealth"
puts "The lengith of the string is: #{str1.length}"

# Get the reverse of the string
puts "The reverse of the string is: #{str1.reverse}"

# Get the uppercase of the string
puts "The uppercase of the string is: #{str1.upcase}"

# Get the lowercase of the string
puts "The lowercase of the string is: #{str1.downcase}"



